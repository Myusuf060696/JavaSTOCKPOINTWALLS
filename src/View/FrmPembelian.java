    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        p = new EntityPembelian();
        p.setKode_pembelian(txtKodePembelian.getText());
        p.setKode_pegawai(txtKodepegawai.getText());
        p.setTanggal(txtTanggal.getText());
        pembelianDAO.insertPembelian(p);
        
        //insert detail pembelian
        int baris = tabelPembelian.getRowCount();
        for(int i=0;i<baris;i++){
            p.setKode_produk(dtmPembelian.getValueAt(i, 0).toString());
            p.setQty(Integer.parseInt(dtmPembelian.getValueAt(i, 3).toString()));
            p.setHarga_satuan(Integer.parseInt(dtmPembelian.getValueAt(i, 2).toString()));
            p.setTotal(Integer.parseInt(dtmPembelian.getValueAt(i, 4).toString()));
            pembelianDAO.insertDetailPembelian(p);
       
        }
        aturButton(true);
        teksKosong();
        aturTabel();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        if(btnExit.getText().equals("Exit")){
            FrmTampilPembelian p = new FrmTampilPembelian();
            JDesktopPane dp = getDesktopPane();
            dp.add(p);
            p.setVisible(true);
            this.dispose();

        }else{
            aturButton(true);
            teksKosong();
            txtKodeProduk.setText("");
            txtNamaProduk.setText("");
            txtHargaSatuan.setText("");
            txtQty.setText("");
            txtTotal.setText("");
            aturTabel();
        }
    }//GEN-LAST:event_btnExitActionPerformed

