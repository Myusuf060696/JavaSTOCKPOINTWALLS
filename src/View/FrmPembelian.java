   private void refreshIsitabelPegawai(){
        listPegawai = pegawaiDAO.selectPegawai(txtCariPegawai.getText(), txtCariPegawai.getText());
        dtmkodePegawai = (DefaultTableModel) tabelCariPegawai.getModel();
        dtmkodePegawai.setRowCount(0);
        for(EntityPegawai data : listPegawai){
            dtmkodePegawai.addRow(new Object[]{data.getKode_pegawai(),data.getNama()});
        }
    }
   private void refreshIsitabelProduk(){
       listProduk = produkDAO.selectProduk(txtCarikodeProduk.getText(), txtCarikodeProduk.getText());
       dtmKodeProduk = (DefaultTableModel) tabelcarikodeProduk.getModel();
       dtmKodeProduk.setRowCount(0);
       for(EntityProduk data : listProduk){
           dtmKodeProduk.addRow(new Object[]{data.getKode_produk(),data.getNama_produk(),data.getHarga_beli_satuan()});
       }
   }

