    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(tabelPembelian.getSelectedRow() >= 0){
            dtmPembelian.removeRow(tabelPembelian.getSelectedRow());
            
            int baris = tabelPembelian.getRowCount();
            total_beli = 0;
            for (int i = 0; i < baris; i++) {
                total_beli = total_beli + Integer.parseInt(dtmPembelian.getValueAt(i, 4).toString());
            }
            txtTotalbeli.setText(String.valueOf(total_beli));
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if(txtTotal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Kok Total nya Kosong ya?");
            txtQty.requestFocus();
        }else{
        dtmPembelian.addRow(new Object[]{txtKodeProduk.getText(),txtNamaProduk.getText(),txtHargaSatuan.getText(),txtQty.getText(),txtTotal.getText()});
        txtKodeProduk.setText("");
        txtNamaProduk.setText("");
        txtHargaSatuan.setText("");
        txtQty.setText("");
        txtTotal.setText("");
        
        int baris = tabelPembelian.getRowCount();
        total_beli = 0;
        for(int i=0;i<baris;i++){
            total_beli = total_beli + Integer.parseInt(dtmPembelian.getValueAt(i, 4).toString());
        }
        txtTotalbeli.setText(String.valueOf(total_beli));
        }
    }//GEN-LAST:event_btnInsertActionPerformed
    
    

